/// Yellow gradient from bright to deep gold (top to bottom)
pub static COLORED: LazyLock<String> = LazyLock::new(|| {
    let mut result = String::with_capacity(ART.len() + 256);
    for (i, line) in ART.lines().enumerate() {
        // 256-color yellow gradient: 229→227→226→220→214→178
        let code = match i {
            0..=2 => 229,
            3..=5 => 227,
            6..=8 => 226,
            9..=11 => 220,
            12..=14 => 214,
            _ => 178,
        };
        let color = format!("\x1b[38;5;{code}m");
        result.push_str(&color);
        result.push_str(line);
        result.push_str("\x1b[0m\n");
    }
    result
});

pub fn print_banner() {
    if io::stdout().is_terminal() {
        let _ = write!(io::stdout(), "{}", *COLORED);
    }
}
